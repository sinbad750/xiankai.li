void Wireless_Upg_Init(void)
{

    unsigned int i, darr_code_cnt, darr_code_wr_sz;
    unsigned int j, maddr;
    unsigned char n;
    Wireless_Reset();
    I2C_Write_Byte2(MCUCC2_ADDR, 0xFE);
    I2C_Write_Byte2(ISCLC_ADDR, 24);
    I2C_Write_Byte2(ISDLC_ADDR, 0x01);
    I2C_Write_Byte2(MPRR_ADDR, 0x01);
    Wait_For_mSec(1000);  // 20ms

    WirelessUpgStep=Upg_Step_Setup;
    rx_setup_data_count=0x00;

    y_pwm_value=LIGHTLY_4P4;
    r_pwm_value=LIGHTLY_4P4;
    w_pwm_value=LIGHTLY_4P4;

    darr_code_cnt = 0;
    while (darr_code_cnt < CODE_DATA_SZ)
    {
        if ((CODE_DATA_SZ - darr_code_cnt) >= MAX_TX_SIZE)
        {
            darr_code_wr_sz = MAX_TX_SIZE;
        }
        else
        {
            darr_code_wr_sz = CODE_DATA_SZ - darr_code_cnt;
        }
        for (i=0; i<darr_code_wr_sz; i++)
        {
            j = i + darr_code_cnt;
            I2C_TX_Data_send_array[i] = codedata11[j];
        }
        maddr = 0x2000 + darr_code_cnt;
        I2C_Write_Buf2(maddr, I2C_TX_Data_send_array, darr_code_wr_sz);
        darr_code_cnt += darr_code_wr_sz;
    }

    I2C_Write_Byte2(MPRR_ADDR, 0x00); // Disable Remap
    I2C_Write_Byte2(ISCLC_ADDR, 24);
    I2C_Write_Byte2(MCUCC2_ADDR, 0xFF);
    I2C_Write_Byte2(ISDLC_ADDR, 0x01);

    while(!Waitfor_ISDLC_ADDR(0x01))  ;
    ;
    I2C_Write_Byte2(ISCLC_ADDR, 22);
    I2C_Write_Byte2(ISDLC_ADDR, 0x51);
    I2C_Write_Byte2(ISCLC_ADDR, 23);
    I2C_Write_Byte2(ISDLC_ADDR, 0x20);
    I2C_Write_Byte2(ISCLC_ADDR, 24);
    I2C_Write_Byte2(ISDLC_ADDR, 0x03);

    while(!Waitfor_ISDLC_ADDR(0x01))  ;
    ;
    I2C_Write_Byte2(ISCLC_ADDR, 24);
    I2C_Write_Byte2(ISDLC_ADDR, 0x00);
    while(!Waitfor_ISDLC_ADDR(0x40))  ;
    ;


    for(n=0; n<28; n++) //24 96K
    {
        debugN=n;
        vLed_Display_Erase(n);
        PassThrough_Sector_Erase(n*0x10);
    }
    Save_default_Code_To_Eeprom();
    Wait_For_mSec(2000);
    y_pwm_value=LIGHTLY_0P4;
    r_pwm_value=LIGHTLY_0P4;
    w_pwm_value=LIGHTLY_0P4;
    wireless_upg_addr=0x0000;
    WirelessUpgStep=Upg_Step_Transmit_Data;
    Updata_Start();
}

