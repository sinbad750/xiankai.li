u8 Waitfor_ISDLC_ADDR(u8 expect_dat)
{
    u16 i;
    i = 1000;
    RX_Data_recv_array[0] =0 ;
    I2C_Read_Buf2(ISDLC_ADDR, 1);	// Returns I2C read data to RX_Data_recv_array buffer.
    while (RX_Data_recv_array[0] != expect_dat && i)
    {
        Wait_For_mSec(1);
        I2C_Read_Buf2(ISDLC_ADDR, 1);
        i--;
    }
    return i;




}
void Waitfor_GPCS41_ADDR(void)
{
    u8 j;
    RX_Data_recv_array[0] = 0 ;
    j = 100;
    I2C_Read_Buf2(GPCS41_ADDR, 1);
    while ((RX_Data_recv_array[0] & ~0x80) && j)
    {
        I2C_Read_Buf2(GPCS41_ADDR, 1);
        Wait_For_mSec(100); // speed up
        j--;
    }
}



void PassThrough_Sector_Erase( u32  map_addr)
{
    u8 tx_dat[3];
    Waitfor_GPCS41_ADDR();
#if 1
    tx_dat[0] = 0x00;
    tx_dat[1] = (unsigned char)(map_addr );
    tx_dat[2] = (unsigned char)(map_addr >>8);
#else
    tx_dat[0] = (unsigned char)(map_addr );
    tx_dat[1] = (unsigned char)(map_addr >>8);
    tx_dat[2] = (unsigned char)(map_addr >>16);
#endif
    I2C_Write_Buf2(0x1C02, tx_dat, 3);
    I2C_Write_Byte2(GPCS41_ADDR, 0x82);
}




unsigned char PassThrough_Write(u32 map_addr, unsigned char *write_buf, unsigned int write_size)
{
    u8 tx_dat1[5];
    Waitfor_GPCS41_ADDR();
    I2C_Write_Buf2(0x0000, write_buf, write_size);

    tx_dat1[0] = (unsigned char)(map_addr);
    tx_dat1[1] = (unsigned char)(map_addr >> 8);
    tx_dat1[2] = (unsigned char)(map_addr >> 16);

    tx_dat1[3] = (unsigned char)(write_size);
    tx_dat1[4] = (unsigned char)(write_size >> 8);
    I2C_Write_Buf2(0x1C02, tx_dat1, 5);
    I2C_Write_Byte2(GPCS41_ADDR, 0x81);
    Waitfor_GPCS41_ADDR();
    return (0);
}
